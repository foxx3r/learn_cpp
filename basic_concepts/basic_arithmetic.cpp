#include <iostream>
using namespace std;

int main()
{
    // sum
    int x = 40 + 60;
    cout << x;

    // subtraction
    x = 100 - 60;
    cout << x;

    // multiplication
    x = 5 * 6;
    cout << x;

    // division
    x = 10 / 3;
    cout << x;

    // modulus
    x = 25 % 7;
    cout << x;

    // operator precedence
    x = (5 + 2) *2;
    cout << x;

    return 0;
}
